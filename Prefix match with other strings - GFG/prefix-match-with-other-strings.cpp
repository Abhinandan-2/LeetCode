class Solution{   
public:
    int klengthpref(string a[], int n, int k, string s){ 
        int len = s.size(), ans =0 ;
        if(k>len) return 0;
        string match =  s.substr(0,k) ;
        for(int i=0;i<n;i++) if(a[i].size()>=k&&a[i].substr(0,k)==match) ans++;
        return ans;
    }
};

