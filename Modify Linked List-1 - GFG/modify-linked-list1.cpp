class Solution{
    public:
    void modify(Node *a,Node **b,int &f){
        if(!a) return ;
        modify(a->next,b,f);
        if(a==(*b)||a->next==(*b)) f=0;
        if(f==0) return ;
        swap((*b)->data,a->data);
        (*b)->data -= a->data;
        (*b) = (*b)->next;
        return ;
    }
    struct Node* modifyTheList(struct Node *a){
        Node *temp = a;
        int f = 1;
        modify(a,&temp,f);
        return a;
    }
};

