class Solution{   
public:
    int appleSequences(int n, int m, string a){
        int i=0 , j =0,ans=0 ;
        while(i<n){
            if(a[i]=='O'){
                if(m>0) m--;
                else while(a[j++]=='A') {}
            }
            ans = max(ans,i-j+1);
            i++;
        }
        return ans;
    }
};

